package com.psb.aidproject.dto;

public class HomeDto {
	private String add;
	private float longi;
	private float lat;
	private int time;
	private String juri;
	private String cons;
	
	public String getAdd() {
		return add;
	}
	public void setAdd(String add) {
		this.add = add;
	}
	public float getLongi() {
		return longi;
	}
	public void setLongi(float longi) {
		this.longi = longi;
	}
	public float getLat() {
		return lat;
	}
	public void setLat(float lat) {
		this.lat = lat;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	public String getJuri() {
		return juri;
	}
	public void setJuri(String juri) {
		this.juri = juri;
	}
	public String getCons() {
		return cons;
	}
	public void setCons(String cons) {
		this.cons = cons;
	}
	
}
